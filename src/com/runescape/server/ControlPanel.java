	@Override
	public final void actionPerformed(final ActionEvent ae) {
		if (ae.getActionCommand().equals("Choose Revision")) {
			final String value = this.getRevisionList().getSelectedValue();
			switch (value) {
			case "317":
				ServerMain.getMain().setRevisionType(RevisionType.THREE_ONE_SEVEN);
				System.out.println("Revision #317 was chosen!");
				break;
			case "562":
				ServerMain.getMain().setRevisionType(RevisionType.FIVE_SIXTY_TWO);
				System.out.println("Revision #562 was chosen!");
				break;
			}

			/*
			 * Create the Networking Frame to choose a network
			 * from the list.
			 */
			SwingUtilities.invokeLater(new Runnable() {
				@Override
				public void run() {
					ControlPanel.this.setNetworkingFrame(new NetworkingFrame());
				}
			});
		}
	}

